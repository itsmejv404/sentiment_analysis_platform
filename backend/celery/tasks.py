@celery_app.task(name="tasks.send_account_deletion_feedback")
def send_account_deletion_feedback(user_email, feedback_text):
    subject = f"[BrandPulse Feedback] Account deleted by {user_email}"
    feedback = (feedback_text or "").strip() or "No feedback provided."
    body = f"""
BrandPulse Account Deletion Feedback

User: {user_email}
Deleted at: {datetime.utcnow().isoformat()}Z

Feedback:
{feedback}
"""
    send_email(to_email=config.DEFAULT_FROM_EMAIL, subject=subject, body=body)


@celery_app.task(name="tasks.send_organization_deletion_email")
def send_organization_deletion_email(owner_email, org_details):
    subject = f"[BrandPulse Notice] Organization deleted: {org_details.get('name', 'Unknown')}"
    body = f"""
BrandPulse Organization Deletion Notice

Owner: {owner_email}
Deleted at: {datetime.utcnow().isoformat()}Z

Organization details:
- ID: {org_details.get('id')}
- Name: {org_details.get('name')}
- Brand Name: {org_details.get('brand_name')}
- Keywords: {org_details.get('keywords')}
- Status before delete: {'Active' if org_details.get('is_active') else 'Inactive'}
"""
    send_email(to_email=owner_email, subject=subject, body=body)